// --------------------------------------------------------------------------------
// Document Ready
// --------------------------------------------------------------------------------
(() => {

  // --------------------------------------------------------------------------------
  // Show scroll button at Distannce
  // --------------------------------------------------------------------------------
  $(document).scroll(function () {
    const scroll = $(this).scrollTop();

    if (scroll > 600) {
      $('#goto-top').removeClass('hide');
    } else {
      $('#goto-top').addClass('hide');
    }
  });

  // --------------------------------------------------------------------------------
  // Scroll to Top
  // --------------------------------------------------------------------------------

  $('a[href=\'#top\']').click(() => {
    $('html, body').animate({scrollTop: 0}, 'slow');
    return false;
  });

  // --------------------------------------------------------------------------------

});
